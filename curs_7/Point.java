public class Point {

    private int x;
    private int y;
    
    public void setX(int x) {
        this.x = x;
    }
    
    public int getX() { // isX
        return this.x;
    }
    
    public void setY(int y) {
        this.y = y;
    }
    
    public int getY() {
        return this.y;
    }
}