public class Cat {

    private int age;
    private String name;
    private boolean dead;
    
    public void setAge(int age) {
        this.age = age;
    }
    
    public int getAge() {
        return this.age;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public String getName() {
        return this.name;
    }
    
    public void setDead(boolean dead) {
        this.dead = dead;
    }
    
    public boolean isDead() {
        return this.dead;
    }
}